// Helper functions
export const getBlogPostBySlug = (slug: string): IBlogPost | undefined => {
  return BLOG_POSTS.find(post => post.slug === slug);
};

export const getBlogPostsByCategory = (category: IBlogPost['category']): IBlogPost[] => {
  return BLOG_POSTS.filter(post => post.category === category);
};

export const getBlogPostsByTag = (tag: string): IBlogPost[] => {
  return BLOG_POSTS.filter(post => post.tags.includes(tag));
};

export const getFeaturedBlogPosts = (): IBlogPost[] => {
  return BLOG_POSTS.filter(post => post.featured).slice(0, 3);
};

export const getLatestBlogPosts = (limit: number = 6): IBlogPost[] => {
  return [...BLOG_POSTS]
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, limit);
};

export const getAllTags = (): string[] => {
  const tags = new Set<string>();
  BLOG_POSTS.forEach(post => {
    post.tags.forEach(tag => tags.add(tag));
  });
  return Array.from(tags).sort();
};

// Pagination helper
export const paginateBlogPosts = (posts: IBlogPost[], page: number, perPage: number = 9) => {
  const start = (page - 1) * perPage;
  const end = start + perPage;
  return {
    posts: posts.slice(start, end),
    totalPages: Math.ceil(posts.length / perPage),
    currentPage: page,
    hasMore: end < posts.length,
  };
};